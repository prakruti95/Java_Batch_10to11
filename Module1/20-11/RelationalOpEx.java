package tops20;

public class RelationalOpEx 
{
	public static void main(String[] args) 
	{
		
		int a = 6;
		int b = 3;
		
		//boolean-true,false
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
	}
}
